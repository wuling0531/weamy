package com.waemy.web.vo.response;

public class ActivityBaseMusicInfoVO {
    
    private String singer;
    
    private String singer_cover;
    
    private String music_name;
    
    private String base_music_id;
    
    public String getSinger() {
        return singer;
    }
    
    public void setSinger(String singer) {
        this.singer = singer;
    }
    
    public String getSinger_cover() {
        return singer_cover;
    }
    
    public void setSinger_cover(String singer_cover) {
        this.singer_cover = singer_cover;
    }
    
    public String getMusic_name() {
        return music_name;
    }
    
    public void setMusic_name(String music_name) {
        this.music_name = music_name;
    }
    
    public String getBase_music_id() {
        return base_music_id;
    }
    
    public void setBase_music_id(String base_music_id) {
        this.base_music_id = base_music_id;
    }
    
}
