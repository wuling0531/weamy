package com.waemy.web.vo.response;

public class ActivityArticleVO {
    
    private String article_id;
    
    private String num;
    
    private String link_url;
    
    private String pic_url;
    
    private String base_music_id;
    
    public String getArticle_id() {
        return article_id;
    }
    
    public void setArticle_id(String article_id) {
        this.article_id = article_id;
    }
    
    public String getNum() {
        return num;
    }
    
    public void setNum(String num) {
        this.num = num;
    }
    
    public String getLink_url() {
        return link_url;
    }
    
    public void setLink_url(String link_url) {
        this.link_url = link_url;
    }
    
    public String getPic_url() {
        return pic_url;
    }
    
    public void setPic_url(String pic_url) {
        this.pic_url = pic_url;
    }
    
    public String getBase_music_id() {
        return base_music_id;
    }
    
    public void setBase_music_id(String base_music_id) {
        this.base_music_id = base_music_id;
    }
    
}
